package com.vic.randomgen.domain;

public class NumberRange {
	private long minval;
	private long maxval;
	
	public long getMinval() {
		return minval;
	}
	public void setMinval(long minval) {
		this.minval = minval;
	}
	public long getMaxval() {
		return maxval;
	}
	public void setMaxval(long maxval) {
		this.maxval = maxval;
	}
		
	
	
}
